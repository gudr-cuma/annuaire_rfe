-- Référentiels AGC / Fédération (codes -> UA_LIB100), exclusions persistantes de dossiers,
-- et import_meta restructurée en une ligne par jeu de données (les imports deviennent
-- indépendants : dossiers / facturation (« Annuaire ») / agc / federation).

CREATE TABLE IF NOT EXISTS agc_ref (
  code        TEXT PRIMARY KEY,
  nom         TEXT,
  imported_at TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS federation_ref (
  code        TEXT PRIMARY KEY,
  nom         TEXT,
  imported_at TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS excluded_dossiers (
  dossier_code TEXT PRIMARY KEY,
  excluded_at  TEXT NOT NULL
);

DROP TABLE IF EXISTS import_meta;
CREATE TABLE import_meta (
  dataset     TEXT PRIMARY KEY CHECK (dataset IN ('dossiers', 'facturation', 'agc', 'federation')),
  imported_at TEXT NOT NULL,
  row_count   INTEGER NOT NULL,
  filename    TEXT
);

